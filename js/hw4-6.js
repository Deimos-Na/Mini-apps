let friday = 6

let monthDay = 30

for (let day = friday; day <= monthDay; day += 7) {
  console.log(`Сегодня пятница, ${day}-е число. Неоходимо подготовить отчет`)
}

for (let friday = 6; friday <= 31; friday += 7) {
  console.log(
    `Сегодня пятница, ${friday}-е число. Необходимо подготовить отчет`
  )
}
